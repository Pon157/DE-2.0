"""Юридические тексты, которые показываются в дочерних ботах: политика
конфиденциальности, пользовательское соглашение (оферта) и политика
возвратов. Показываются по команде /info в любое время, а также ОДИН РАЗ —
перед первым использованием бота, с обязательным подтверждением (см.
child/common.py::terms_gate). Тексты — типовые шаблоны для сервиса
Telegram-ботов с приёмом сообщений/донатов через Telegram Stars и, при
подключении, оплатой через ЮKassa. Если у владельца бота есть юрлицо/ИП и
особые условия — тексты стоит адаптировать под них, это отправная база.
"""

PRIVACY_POLICY = """<b>Политика конфиденциальности</b>

1. Настоящая политика определяет порядок обработки персональных данных \
пользователей бота @{username} (далее — «Бот»).

2. Используя Бота, вы соглашаетесь с передачей и обработкой следующих \
данных: ваш Telegram ID, username (если указан), имя профиля, а также \
содержание сообщений, которые вы отправляете Боту (текст, фото, видео, \
файлы и т.п.).

3. Данные используются исключительно для работы Бота: обработки ваших \
обращений, показа ответов администрации, модерации (варны/баны при \
нарушении правил), статистики использования и связи с вами по вашим же \
сообщениям.

4. Данные не передаются третьим лицам, за исключением случаев, \
предусмотренных законодательством РФ, либо при использовании \
сторонних сервисов оплаты (ЮKassa, Telegram Payments) — им передаются \
только данные, необходимые для проведения платежа.

5. Вы можете в любой момент прекратить использование Бота, заблокировав \
его в Telegram — новые сообщения обрабатываться не будут.

6. По вопросам обработки данных обращайтесь к администрации Бота через \
чат поддержки."""


USER_AGREEMENT = """<b>Пользовательское соглашение</b>

1. Используя Бота @{username}, вы подтверждаете, что ознакомлены и \
согласны с условиями настоящего соглашения, Политикой конфиденциальности \
и Политикой возвратов.

2. Бот предоставляется «как есть». Администрация Бота не гарантирует \
бесперебойную работу и не несёт ответственности за временную \
недоступность сервиса Telegram.

3. Запрещается: рассылать спам, оскорблять администрацию и других \
пользователей, присылать материалы, нарушающие законодательство РФ и \
Условия использования Telegram. За нарушение администрация вправе \
выдать предупреждение либо заблокировать доступ к Боту без объяснения \
причин.

4. Донаты (Telegram Stars) и иные платежи, предусмотренные функционалом \
Бота, являются добровольными и совершаются на условиях, описанных в \
Политике возвратов.

5. Администрация вправе изменять функционал Бота и настоящее соглашение \
в любой момент. Продолжение использования Бота после изменений означает \
согласие с новой редакцией."""


REFUND_POLICY = """<b>Политика возвратов</b>

1. Донаты через Telegram Stars, отправленные добровольно в знак \
поддержки, по общему правилу возврату не подлежат, так как не являются \
оплатой конкретного товара или услуги.
Исключение — техническая ошибка (двойное списание, сбой платежа): в \
этом случае напишите в чат поддержки бота с указанием даты и суммы, \
возврат Stars оформляется через @Spam_Bot в течение 21 дня с момента \
платежа (ограничение самого Telegram) либо, если оплата проходила через \
ЮKassa — согласно её правилам ниже.

2. Если функционал Бота предусматривает оплату через ЮKassa: возврат \
возможен в течение 14 дней с момента оплаты, если услуга фактически не \
была оказана. Для оформления возврата напишите в чат поддержки бота с \
номером платежа — заявка рассматривается в течение 10 рабочих дней \
согласно правилам ЮKassa и Закону РФ «О защите прав потребителей».

3. Возврат не производится, если услуга (например, публикация \
рекламного объявления, показ поста) уже была фактически оказана \
полностью или частично.

4. Спорные случаи рассматриваются администрацией Бота индивидуально — \
пишите в чат поддержки."""


def info_text(username: str) -> str:
    """Полный текст для команды /info — все три документа одним сообщением."""
    return (PRIVACY_POLICY.format(username=username) + "\n\n" +
            USER_AGREEMENT.format(username=username) + "\n\n" +
            REFUND_POLICY.format(username=username))


def gate_text(username: str) -> str:
    """Короткая версия для экрана согласия перед первым использованием —
    полные тексты доступны по /info, тут только факт и кнопка."""
    return (f"👋 Прежде чем начать пользоваться @{username}, ознакомьтесь и "
           "примите условия:\n\n"
           "📄 Политика конфиденциальности\n"
           "📄 Пользовательское соглашение\n"
           "📄 Политика возвратов\n\n"
           "Полные тексты — по команде /info.\n\n"
           "Нажимая «Принимаю», вы подтверждаете согласие со всеми "
           "тремя документами.")
