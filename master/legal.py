"""Юридические тексты для САМОГО КОНСТРУКТОРА (master-бота) — не для
дочерних ботов. Показываются по команде /info в любое время, а также ОДИН
РАЗ — перед первым использованием конструктора, с обязательным
подтверждением (см. master/router.py — гейт в _master_guard_message).
Типовые шаблоны для сервиса создания Telegram-ботов с оплатой рекламы/донатов
через Telegram Stars и, при подключении, через ЮKassa. При необходимости
адаптируйте под юрлицо/ИП владельца платформы."""

PRIVACY_POLICY = """<b>Политика конфиденциальности</b>

1. Настоящая политика определяет порядок обработки персональных данных \
пользователей конструктора ботов (далее — «Платформа»).

2. Используя Платформу, вы соглашаетесь с обработкой следующих данных: \
ваш Telegram ID, username (если указан), имя профиля, содержимое \
настроек создаваемых вами ботов, а также техническая информация об их \
работе (статистика, логи ошибок).

3. Данные используются исключительно для работы Платформы: создания и \
обслуживания ваших ботов, обработки платежей за рекламу/подписку, связи \
с вами по вопросам работы сервиса, модерации нарушений.

4. Данные не передаются третьим лицам, за исключением случаев, \
предусмотренных законодательством РФ, либо при использовании сторонних \
сервисов оплаты (ЮKassa, Telegram Payments) — им передаются только \
данные, необходимые для проведения платежа.

5. Вы можете в любой момент прекратить использование Платформы, \
заблокировав master-бота — новые данные собираться не будут (уже \
созданные вами боты продолжат работать самостоятельно до их удаления).

6. По вопросам обработки данных обращайтесь к администрации Платформы."""


USER_AGREEMENT = """<b>Пользовательское соглашение</b>

1. Используя конструктор ботов, вы подтверждаете, что ознакомлены и \
согласны с условиями настоящего соглашения, Политикой конфиденциальности \
и Политикой возвратов.

2. Платформа предоставляется «как есть». Администрация не гарантирует \
бесперебойную работу и не несёт ответственности за временную \
недоступность сервиса Telegram, а также за содержание и действия \
ботов, созданных пользователями Платформы.

3. Запрещается: создавать ботов, нарушающих законодательство РФ и \
Условия использования Telegram, рассылать спам, размещать рекламу \
запрещённых товаров и услуг. За нарушение администрация вправе \
заблокировать доступ к Платформе без объяснения причин и без возврата \
уплаченных средств за уже оказанные услуги.

4. Реклама, донаты и иные платежи, предусмотренные функционалом \
Платформы, совершаются на условиях, описанных в Политике возвратов.

5. Администрация вправе изменять функционал Платформы и настоящее \
соглашение в любой момент. Продолжение использования Платформы после \
изменений означает согласие с новой редакцией."""


REFUND_POLICY = """<b>Политика возвратов</b>

1. Оплата показов рекламы, рассылок и донатов через Telegram Stars, \
внесённая добровольно, по общему правилу возврату не подлежит после \
того, как услуга начала оказываться (реклама одобрена и/или начала \
показываться). Исключение — техническая ошибка (двойное списание, сбой \
платежа): напишите в поддержку с датой и суммой, возврат Stars \
оформляется при помощи самой платформы Telegram в течение 21 дня с момента платежа \
(ограничение самого Telegram).

2. Если реклама была ОТКЛОНЕНА модерацией уже после оплаты — стоимость \
возвращается в полном объёме.

3. Если функционал Платформы предусматривает оплату через ЮKassa: \
возврат возможен в течение 14 дней с момента оплаты, если услуга \
фактически не была оказана. Для оформления возврата напишите в \
поддержку с номером платежа — заявка рассматривается в течение 10 \
рабочих дней согласно правилам ЮKassa и Закону РФ «О защите прав \
потребителей».

4. Возврат не производится, если услуга (показ рекламы, рассылка) уже \
была оказана полностью или частично — например, если часть \
оплаченных показов уже состоялась.

5. Спорные случаи рассматриваются администрацией индивидуально."""


def info_text() -> str:
    """Полный текст для команды /info — все три документа одним сообщением."""
    return PRIVACY_POLICY + "\n\n" + USER_AGREEMENT + "\n\n" + REFUND_POLICY


